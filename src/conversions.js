// Kilometers Conversions
export const kmToNm = km => parseFloat(km) * 100000000;
export const kmToUm = km => parseFloat(km) * 10000000;
export const kmToMm = km => parseFloat(km) * 1000000;
export const kmToCm = km => parseFloat(km) * 100000;
export const kmToDm = km => parseFloat(km) * 10000;
export const kmToM = km => parseFloat(km) * 1000;
export const kmToDam = km => parseFloat(km) * 100;
export const kmToHm = km => parseFloat(km) * 10;
export const kmToMile = km => parseFloat(km) * 0.62;
export const kmToFoot = km => parseFloat(km) * 3280.8;
export const kmToInch = km => parseFloat(km) * 39370.079;
export const kmToNauticMile = km => parseFloat(km) * 1.852;
export const kmToYard = km => parseFloat(km) * 1093.613;
export const kmToRod = km => parseFloat(km) * 198.939;
export const kmToFathom = km => parseFloat(km) * 546.807;
export const kmToFurlong = km => parseFloat(km) * 4.971;
